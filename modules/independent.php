<p class=MsoNormal style='line-height:125%;background:white'><span
style='font-size:10.0pt;mso-bidi-font-size:9.0pt;line-height:125%;font-family:
"Arial","sans-serif";mso-fareast-font-family:"Times New Roman";color:black'>Jean
King has been in the forefront of both environmental preservation and promotion
of the arts.<o:p></o:p></span></p>

<p class=MsoNormal style='line-height:125%;background:white'><span
style='font-size:10.0pt;mso-bidi-font-size:9.0pt;line-height:125%;font-family:
"Arial","sans-serif";mso-fareast-font-family:"Times New Roman";color:black'>It
is hard to imagine what Livermore and the Valley would have become had it not
been for the two of them.<o:p></o:p></span></p>

<p class=MsoNormal style='line-height:125%;background:white'><span
style='font-size:10.0pt;mso-bidi-font-size:9.0pt;line-height:125%;font-family:
"Arial","sans-serif";mso-fareast-font-family:"Times New Roman";color:black'>Jean
King served on the boards of the League of Women Voters, AAUW, Friends of the
Vineyards; Del Valle Fine Arts, Livermore-Amador Symphony Association; and on
the boards of Tri-Valley Conservancy, Livermore Commission for the Arts, and
Livermore Cultural Arts Council. She was named Woman of the Year (2009) for the
15th Assembly District, for her leadership role in the arts and the
environment. She was engaged in all of Livermore's battles to protect open
space and the environment.<o:p></o:p></span></p>

<p class=MsoNormal style='line-height:125%;background:white'><span
style='font-size:10.0pt;mso-bidi-font-size:9.0pt;line-height:125%;font-family:
"Arial","sans-serif";mso-fareast-font-family:"Times New Roman";color:black'>King
was employed at Sandia Labs as a computer programmer and is an adjunct
instructor at Las <span class=SpellE>Positas</span> College teaching
mathematics.<o:p></o:p></span></p>

<p class=MsoNormal style='line-height:125%;background:white'><span
style='font-size:10.0pt;mso-bidi-font-size:9.0pt;line-height:125%;font-family:
"Arial","sans-serif";mso-fareast-font-family:"Times New Roman";color:black'>It
has been said that the Tri-Valley environmental movement was born in Miller's
living room in the early 1960's. After helping to elect several like-minded
citizens to Livermore's city council in 1972, he was instrumental in launching
the SAVE initiative in Livermore, a measure that blocked development unless
adequate services were identified. His efforts, and those of others, culminated
in the adoption of the Alameda County urban growth boundary (UGB) in 2000 and
the Livermore UGB two years later. The first threat to the UGB was rebuffed in
2005, when Miller and his allies defeated the <span class=SpellE>Pardee</span>
Homes plan for 12,500 homes with the overwhelming support of 72 percent of
Livermore voters.<o:p></o:p></span></p>

<p class=MsoNormal style='line-height:125%;background:white'><span
style='font-size:10.0pt;mso-bidi-font-size:9.0pt;line-height:125%;font-family:
"Arial","sans-serif";mso-fareast-font-family:"Times New Roman";color:black'>Miller
is the father of Livermore's efforts to protect the area's heritage with a ring
of open space that surrounds the city. Thanks to him the rolling hills and
valleys are home to vineyards, working ranches and wildlife.<o:p></o:p></span></p>

<p class=MsoNormal style='line-height:125%;background:white'><span
style='font-size:10.0pt;mso-bidi-font-size:9.0pt;line-height:125%;font-family:
"Arial","sans-serif";mso-fareast-font-family:"Times New Roman";color:black'>He
served as a city councilmember for eight years and mayor for one. <span
class=GramE>Whether in office or out, his passion inspired others.</span><o:p></o:p></span></p>

<p class=MsoNormal style='line-height:125%;background:white'><span
style='font-size:10.0pt;mso-bidi-font-size:9.0pt;line-height:125%;font-family:
"Arial","sans-serif";mso-fareast-font-family:"Times New Roman";color:black'>In
the realm of the arts, Miller's role was extensive. He belonged to Cask and
Mask, the original theater group in Livermore, and the board of Del Valle Fine
Arts, which presents concerts featuring internationally known musicians. He is
an accomplished pianist. He composed Centennial Rag, a ragtime piano tune, in
celebration of Livermore's Centennial. He, along with a few others founded the
Livermore Cultural Arts Council in the 1960's, which recognized him and King
for their service at a ceremony last week.<o:p></o:p></span></p>

<p class=MsoNormal style='line-height:125%;background:white'><span
