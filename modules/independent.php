"Arial","sans-serif";mso-fareast-font-family:"Times New Roman";color:black'>As
a Lawrence Livermore National Laboratory Ph.D. scientist from 1956 to
retirement, he became known worldwide for his work in various aspects of
thermodynamics and related fields.<o:p></o:p></span></p>

